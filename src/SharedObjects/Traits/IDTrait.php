<?php
namespace CRM_SDK\SharedObjects\Traits;

trait IDTrait
{
    /** @var int|null */
    private $id;

    /**
     * @return int|null
     */
    public function getId(): ?int
    {
        return $this->id;
    }

    /**
     * @param int|null $id
     * @return self
     */
    public function setId(?int $id): self
    {
        $this->id = $id;
        return $this;
    }
}